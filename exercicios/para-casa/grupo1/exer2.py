# Faça um programa que receba dois numeros e mostre o maior. 
# Se por acaso, os dois números forem iguais, imprima a mensagem "Números iguais".


def mostre_numeros():
    num1 = int(input( "Informe um número1? : " ))
    num2 = int(input( "Informe um número2? : " ))

    if num1 > num2:
        print( f" O numero maior é o {num1}" )
        
    elif num2 > num1 :
        print( f" O numero maior é o {num2}" )
        
        
    else :
        print( f"Números iguais" )
        
        
mostre_numeros()





    





    













        


    

        


