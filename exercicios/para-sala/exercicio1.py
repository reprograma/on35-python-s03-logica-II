#Faça um programa que mostre na tela uma pergunta de múltipla escolha, e que, a partir da resposta do 
#usuário, mostre na tela se ele acertou ou não.

pergunta = input("Qual e minha fruta favorita? a: maça b: banana c: laranja")

if pergunta == "maça":
    print ("Compre uma duzia para mim")

if pergunta == "banana":
    print ("Compre outra fruta")

if pergunta == "laranja":
    print ("Traga apenas meia duzia")

 

 
