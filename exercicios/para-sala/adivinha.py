# 2. Faça um programa que mostre na tela uma pergunta de múltipla escolha, 
# e que, a partir da resposta do usuário, 
# mostre na tela se ele acertou ou não.

adivinha = int(input("Escolha feijão por cima opção nº 1 ou feijão por baixo opção nº 2 ? :"))
if adivinha == 1 :
    print ("Não combina, você está vivendo a vida errada")
elif adivinha == 2 :
    print("É isso combinação perfeita")
else:
    print("Não entendi, tente novamente")
        


